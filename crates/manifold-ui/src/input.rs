    fn rebuild(tree: &mut UITree) {
        tree.clear();
        let root = tree.add_panel(None, 0.0, 0.0, 800.0, 600.0, UIStyle::default());
        tree.add_button(Some(root), 50.0, 50.0, 100.0, 30.0, UIStyle::default(), "Click me");
    }

